// NODE CLASS
struct NodeObj {
  uint16_t Address;
  char SH;
  char SL;
  char Strength;
  char Identifier;
  bool Valid = false;
};

// CONTROLLER CLASS
class Controller {
    public:
    Controller();
    void initOLED();
    void initUSB();
    void initXBee();
    void scanNodes();
    int nodeCount = 0;
    NodeObj nodeObjs[];
    int mode = 0;
    uint16_t addr = 0x0000;

    // allocate two bytes for to hold a 10-bit analog reading
    uint8_t payload[2] = { 0, 0 };
    
    // Inits
    void initOLD();

    private:
    NodeObj Node();
    int timeout = 5000;  // default value of NT (if NT command fails)
    AtCommandRequest request = AtCommandRequest();
    AtCommandResponse response = AtCommandResponse();
};

Controller controller;

Controller::Controller() {
}

